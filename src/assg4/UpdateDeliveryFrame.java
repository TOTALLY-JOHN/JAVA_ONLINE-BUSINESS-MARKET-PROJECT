    // UPDATE BUTTON
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        if(jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "You need to search for an order in advance!");
        }
        else
        {
            try
            {
                for(int i=0; i<Main.orderList.size(); i++)
                {
                    if(Integer.parseInt(jTextField1.getText()) == Main.orderList.get(i).getOrderID())
                    {
                        Main.orderList.get(i).setDeliveryStatus(jComboBox1.getSelectedItem().toString());
                    }
                }
                
                FileWriter fw = new FileWriter("orders.txt", false);
                PrintWriter pw = new PrintWriter(fw);
                
                for(int i=0; i<Main.orderList.size(); i++)
                {
                    pw.println(Main.orderList.get(i).getOrderID());
                    pw.println(Main.orderList.get(i).getCustName());
                    pw.println(Main.orderList.get(i).getProductName());
                    pw.println(Main.orderList.get(i).getProductQuantity());
                    pw.println(Main.orderList.get(i).getTotalPrice());
                    pw.println(Main.orderList.get(i).getDeliveryCharge());
                    pw.println(Main.orderList.get(i).getAddress());
                    pw.println(Main.orderList.get(i).getPhoneNo());
                    pw.println(Main.orderList.get(i).getPaymentMethod());
                    pw.println(Main.orderList.get(i).getDeliveryStatus());
                }
                pw.close();
                JOptionPane.showMessageDialog(null, "Successfully updated!");
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

