    // SEARCH BUTTON
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        boolean orderFound = false;
        if(jTextField1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Your input is blank!");
        }
        else
        {
            try
            {
                for(int i=0; i<Main.orderList.size(); i++)
                {
                    if(Integer.parseInt(jTextField1.getText()) == Main.orderList.get(i).getOrderID())
                    {
                        orderFound = true;
                        String str = "";
                        jTextField2.setText(Main.orderList.get(i).getOrderID() +"");
                        jTextField3.setText(Main.orderList.get(i).getCustName());
                        jTextField4.setText(Main.orderList.get(i).getProductName());
                        jComboBox1.setSelectedItem(Main.orderList.get(i).getDeliveryStatus()); 
                    }
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }

        if(orderFound == false)
        {
            JOptionPane.showMessageDialog(null, "No information found!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

